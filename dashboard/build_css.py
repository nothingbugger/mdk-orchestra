def build_css() -> None:
    """Read tokens.json, write dashboard.css."""
    if not _TOKENS_PATH.exists():
        print(f"ERROR: tokens.json not found at {_TOKENS_PATH}", file=sys.stderr)
        sys.exit(1)

    tokens = json.loads(_TOKENS_PATH.read_text(encoding="utf-8"))

    root_vars = _build_root_vars(tokens)
    semantic_classes = _build_semantic_classes(tokens)

    out = root_vars + "\n" + semantic_classes

    _OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    _OUT_PATH.write_text(out, encoding="utf-8")
    print(f"Built: {_OUT_PATH}  ({len(out):,} bytes)")


if __name__ == "__main__":
    build_css()
