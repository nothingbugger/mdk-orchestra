def _flat(tokens: dict, prefix: str = "") -> dict[str, str]:
    """Flatten nested token dict into CSS variable names → values.

    E.g. {"color": {"bg": {"base": {"value": "#1b2430"}}}}
    → {"--color-bg-base": "#1b2430"}
    """
    result: dict[str, str] = {}
    for key, val in tokens.items():
        # Skip schema / metadata keys
        if key in ("$schema", "name", "description", "version"):
            continue
        css_key = f"{prefix}-{key}" if prefix else key
        if isinstance(val, dict):
            if "value" in val:
                # leaf token
                v = val["value"]
                # CSS var names use hyphens
                var_name = f"--{css_key.replace('.', '-')}"
                result[var_name] = str(v)
            else:
                # recurse
                result.update(_flat(val, css_key))
        # non-dict primitives at top level (e.g. plotly/matplotlib sections) are skipped
    return result


def _build_root_vars(tokens: dict) -> str:
    """Emit :root { ... } block with all token values."""
    flat = _flat(tokens)
    lines = [":root {"]
    for var, val in sorted(flat.items()):
        lines.append(f"  {var}: {val};")
    lines.append("}")
    return "\n".join(lines)


